    <p align="justify">
        <b>1. Ruang Lingkup</b><br>
        a. Media Siber adalah segala bentuk media yang menggunakan wahana internet dan melaksanakankegiatan
        jurnalistik, serta memenuhi persyaratan Undang-Undang Pers dan Standar Perusahaan Pers yang ditetapkan Dewan
        Pers.
        <br>
        b. Isi Buatan Pengguna (User Generated Content) adalah segala isi yang dibuat dan atau dipublikasikan oleh
        pengguna media siber, antara lain, artikel, gambar, komentar, suara, video dan berbagai bentuk unggahan yang
        melekat pada media siber, seperti blog, forum, komentar pembaca atau pemirsa, dan bentuk lain.
        <br> <br>
        <b>2. Verifikasi dan keberimbangan berita</b>
        <br>
        a. Pada prinsipnya setiap berita harus melalui verifikasi.<br>
        b. Berita yang dapat merugikan pihak lain memerlukan verifikasi pada berita yang sama untuk memenuhi prinsip
        akurasi dan keberimbangan.<br>
        c. Ketentuan dalam butir (a) di atas dikecualikan, dengan syarat:<br>
        1) Berita benar-benar mengandung kepentingan publik yang bersifat mendesak;<br>
        2) Sumber berita yang pertama adalah sumber yang jelas disebutkan identitasnya, kredibel dan kompeten;<br>
        3) Subyek berita yang harus dikonfirmasi tidak diketahui keberadaannya dan atau tidak dapat
        diwawancarai;<br>
        4) Media memberikan penjelasan kepada pembaca bahwa berita tersebut masih memerlukan verifikasi lebih lanjut
        yang diupayakan dalam waktu secepatnya. Penjelasan dimuat pada bagian akhir dari berita yang sama, di dalam
        kurung dan menggunakan huruf miring.<br>
        d. Setelah memuat berita sesuai dengan butir (c), media wajib meneruskan upaya verifikasi, dan setelah
        verifikasi didapatkan, hasil verifikasi dicantumkan pada berita pemutakhiran (update) dengan tautan pada
        berita yang belum terverifikasi.
        <br>
        <br>
        <b>3. Isi Buatan Pengguna (User Generated Content)</b>
        <br>
        a. Media siber wajib mencantumkan syarat dan ketentuan mengenai Isi Buatan Pengguna yang tidak bertentangan
        dengan Undang-Undang No. 40 tahun 1999 tentang Pers dan Kode Etik Jurnalistik, yang ditempatkan secara
        terang dan jelas.<br>
        b. Media siber mewajibkan setiap pengguna untuk melakukan registrasi keanggotaan dan melakukan proses log-in
        terlebih dahulu untuk dapat mempublikasikan semua bentuk Isi Buatan Pengguna. Ketentuan mengenai log-in akan
        diatur lebih lanjut.<br>
        c. Dalam registrasi tersebut, media siber mewajibkan pengguna memberi persetujuan tertulis bahwa Isi Buatan
        Pengguna yang dipublikasikan:<br>
        1) Tidak memuat isi bohong, fitnah, sadis dan cabul;<br>
        2) Tidak memuat isi yang mengandung prasangka dan kebencian terkait dengan suku, agama, ras, dan
        antargolongan (SARA), serta menganjurkan tindakan kekerasan;<br>
        3) Tidak memuat isi diskriminatif atas dasar perbedaan jenis kelamin dan bahasa, serta tidak merendahkan
        martabat orang lemah, miskin, sakit, cacat jiwa, atau cacat jasmani.<br>
        e. Media siber memiliki kewenangan mutlak untuk mengedit atau menghapus Isi Buatan Pengguna yang
        bertentangan dengan butir (c).<br>
        f. Media siber wajib menyediakan mekanisme pengaduan Isi Buatan Pengguna yang dinilai melanggar ketentuan
        pada butir (c). Mekanisme tersebut harus disediakan di tempat yang dengan mudah dapat diakses pengguna.<br>
        g. Media siber wajib menyunting, menghapus, dan melakukan tindakan koreksi setiap Isi Buatan Pengguna yang
        dilaporkan dan melanggar ketentuan butir (c), sesegera mungkin secara proporsional selambatlambatnya
        2 x 24 jam setelah pengaduan diterima.<br>
        h. Media siber yang telah memenuhi ketentuan pada butir (a), (b), (c), dan (f) tidak dibebani tanggung jawab
        atas masalah yang ditimbulkan akibat pemuatan isi yang melanggar ketentuan pada butir (c).<br>
        i. Media siber bertanggung jawab atas Isi Buatan Pengguna yang dilaporkan bila tidak mengambil tindakan
        koreksi setelah batas waktu sebagaimana tersebut pada butir (f).<br>
        <br>
        <b>4. Ralat, Koreksi, dan Hak Jawab</b>
        <br>
        a. Ralat, koreksi, dan hak jawab mengacu pada Undang-Undang Pers, Kode Etik Jurnalistik, dan Pedoman Hak
        Jawab yang ditetapkan Dewan Pers.<br>
        b. Ralat, koreksi dan atau hak jawab wajib ditautkan pada berita yang diralat, dikoreksi atau yang diberi
        hak jawab.<br>
        c. Di setiap berita ralat, koreksi, dan hak jawab wajib dicantumkan waktu pemuatan ralat, koreksi, dan atau
        hak jawab tersebut.<br>
        d. Bila suatu berita media siber tertentu disebarluaskan media siber lain, maka:<br>
        1) Tanggung jawab media siber pembuat berita terbatas pada berita yang dipublikasikan di media siber
        tersebut atau media siber yang berada di bawah otoritas teknisnya;<br>
        2) Koreksi berita yang dilakukan oleh sebuah media siber, juga harus dilakukan oleh media siber lain yang
        mengutip berita dari media siber yang dikoreksi itu;<br>
        3) Media yang menyebarluaskan berita dari sebuah media siber dan tidak melakukan koreksi atas berita sesuai
        yang dilakukan oleh media siber pemilik dan atau pembuat berita tersebut, bertanggung jawab penuh atas semua
        akibat hukum dari berita yang tidak dikoreksinya itu.<br>
        e. Sesuai dengan Undang-Undang Pers, media siber yang tidak melayani hak jawab dapat dijatuhi sanksi hukum
        pidana denda paling banyak Rp500.000.000 (Lima ratus juta rupiah).<br>
        <br>
        <b>5. Pencabutan Berita<br></b>
        a. Berita yang sudah dipublikasikan tidak dapat dicabut karena alasan penyensoran dari pihak luar redaksi,
        kecuali terkait masalah SARA, kesusilaan, masa depan anak, pengalaman traumatik korban atau berdasarkan
        pertimbangan khusus lain yang ditetapkan Dewan Pers.<br>
        b. Media siber lain wajib mengikuti pencabutan kutipan berita dari media asal yang telah dicabut.<br>
        c. Pencabutan berita wajib disertai dengan alasan pencabutan dan diumumkan kepada publik.<br>
        <br>
        <b>6. Iklan<br></b>
        a. Media siber wajib membedakan dengan tegas antara produk berita dan iklan.<br>
        b. Setiap berita/artikel/isi yang merupakan iklan dan atau isi berbayar wajib mencantumkan keterangan
        “advertorial”, “iklan”, “ads”, “sponsored”, atau kata lain yang menjelaskan bahwa berita/artikel/isi
        tersebut adalah iklan.<br>
        <br>
        <b>7. Hak Cipta<br></b>
        Media siber wajib menghormati hak cipta sebagaimana diatur dalam peraturan perundang-undangan yang
        berlaku.<br>
        <br>
        <b>8. Pencantuman Pedoman<br></b>
        Media siber wajib mencantumkan Pedoman Pemberitaan Media Siber ini di medianya secara terang dan jelas.<br>
        <br>
        <b>9. Sengketa<br></b>
        Penilaian akhir atas sengketa mengenai pelaksanaan Pedoman Pemberitaan Media Siber ini diselesaikan oleh
        Dewan Pers.<br>
